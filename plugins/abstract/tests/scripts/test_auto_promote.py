class TestCalculatePriority:
    """calculate_priority computes (Frequency x Impact) / Ease."""

    @pytest.mark.unit
    def test_high_failure_rate_high_impact(self):
        """High failure rate yields non-trivial score."""
        item = {
            "type": "high_failure_rate",
            "severity": "high",
            "executions": 10,
            "success_rate": 0.0,  # 0% success = max impact
        }
        score = apl.calculate_priority(item)
        assert score > apl.HIGH_PRIORITY_THRESHOLD

    @pytest.mark.unit
    def test_low_rating_score(self):
        """Low rating yields positive score based on gap from 5.0."""
        item = {
            "type": "low_rating",
            "severity": "medium",
            "executions": 5,
            "avg_rating": 2.0,
        }
        score = apl.calculate_priority(item)
        # Impact = (5.0 - 2.0) * 2 = 6.0; ease = 3.0; freq = 5
        assert score == pytest.approx(5 * 6.0 / 3.0)

    @pytest.mark.unit
    def test_slow_execution_score(self):
        """Slow execution item yields score proportional to seconds over threshold."""
        item = {
            "type": "slow_execution",
            "severity": "medium",
            "executions": 5,
            "avg_duration_ms": 20000,  # 10s over threshold
        }
        score = apl.calculate_priority(item)
        # seconds_over = 10; impact = 1.0; ease = 3.0; freq = 5
        assert score == pytest.approx(5 * 1.0 / 3.0)

    @pytest.mark.unit
    def test_excessive_failures_fixed_high_impact(self):
        """Excessive failures use fixed impact of 8.0."""
        item = {
            "type": "excessive_failures",
            "severity": "high",
            "executions": 5,
        }
        score = apl.calculate_priority(item)
        # impact = 8.0; ease = 2.0; freq = 5
        assert score == pytest.approx(5 * 8.0 / 2.0)

    @pytest.mark.unit
    def test_unknown_type_minimal_impact(self):
        """Unknown type yields minimal positive score."""
        item = {
            "type": "unknown",
            "severity": "low",
            "executions": 1,
        }
        score = apl.calculate_priority(item)
        assert score > 0
        assert score < 1.0

    @pytest.mark.unit
    def test_zero_executions_defaults_to_one(self):
        """Zero executions defaults to frequency=1 to avoid zero scores."""
        item = {"type": "high_failure_rate", "severity": "high", "success_rate": 0.0}
        score_no_exec = apl.calculate_priority(item)
        item_with_exec = {
            "type": "high_failure_rate",
            "severity": "high",
            "executions": 1,
            "success_rate": 0.0,
        }
        score_one_exec = apl.calculate_priority(item_with_exec)
        assert score_no_exec == score_one_exec


