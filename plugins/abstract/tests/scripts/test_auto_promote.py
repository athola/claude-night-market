class TestRunAutoPromote:
    """run_auto_promote orchestrates the full pipeline."""

    @pytest.mark.unit
    def test_returns_empty_when_no_learnings_file(self, tmp_path, monkeypatch):
        """When LEARNINGS.md doesn't exist, returns empty list."""
        monkeypatch.setattr(apl, "get_learnings_path", lambda: tmp_path / "missing.md")
        result = apl.run_auto_promote()
        assert result == []

    @pytest.mark.unit
    def test_returns_empty_when_no_items(self, tmp_path, monkeypatch):
        """When LEARNINGS.md has no promotable items, returns empty list."""
        learnings_path = tmp_path / "LEARNINGS.md"
        learnings_path.write_text("# Empty\n**Skills Analyzed**: 0\n")
        monkeypatch.setattr(apl, "get_learnings_path", lambda: learnings_path)
        result = apl.run_auto_promote()
        assert result == []

    @pytest.mark.unit
    def test_returns_empty_when_repo_undetectable(self, tmp_path, monkeypatch):
        """When detect_target_repo fails, returns empty list."""
        learnings_path = tmp_path / "LEARNINGS.md"
        learnings_path.write_text(SAMPLE_LEARNINGS)
        monkeypatch.setattr(apl, "get_learnings_path", lambda: learnings_path)
        with patch.object(apl, "detect_target_repo", return_value=None):
            result = apl.run_auto_promote()
        assert result == []

    @pytest.mark.unit
    def test_skips_already_promoted_items(self, tmp_path, monkeypatch):
        """Items already in the promoted record are skipped."""
        learnings_path = tmp_path / "LEARNINGS.md"
        learnings_path.write_text(SAMPLE_LEARNINGS)
        monkeypatch.setattr(apl, "get_learnings_path", lambda: learnings_path)

        # Pre-populate record with all possible keys
        items = apl.parse_improvement_items(SAMPLE_LEARNINGS)
        all_keys = {f"{i['skill']}:{i.get('type', 'unknown')}" for i in items}
        pre_record = apl.PromotedIssueRecord(
            promoted=dict.fromkeys(all_keys, "https://existing")
        )

        record_path = tmp_path / "promoted.json"
        pre_record.save(record_path)

        with (
            patch.object(apl, "detect_target_repo", return_value=("o", "r")),
            patch.object(apl, "PromotedIssueRecord") as mock_cls,
        ):
            mock_cls.load.return_value = pre_record
            result = apl.run_auto_promote()

        assert result == []

    @pytest.mark.unit
    def test_promotes_high_priority_to_issue(self, tmp_path, monkeypatch):
        """High-priority items are promoted to GitHub issues."""
        learnings_path = tmp_path / "LEARNINGS.md"
        learnings_path.write_text(SAMPLE_LEARNINGS)
        monkeypatch.setattr(apl, "get_learnings_path", lambda: learnings_path)

        record = apl.PromotedIssueRecord()
        issue_url = "https://github.com/issues/42"

        with (
            patch.object(apl, "detect_target_repo", return_value=("o", "r")),
            patch.object(apl, "PromotedIssueRecord") as mock_cls,
            patch.object(apl, "has_existing_issue", return_value=False),
            patch.object(apl, "calculate_priority", return_value=10.0),
            patch.object(apl, "promote_to_issue", return_value=issue_url),
            patch.object(apl, "post_to_discussion", return_value=None),
        ):
            mock_cls.load.return_value = record
            result = apl.run_auto_promote()

        assert issue_url in result

    @pytest.mark.unit
    def test_skips_items_with_existing_github_issues(self, tmp_path, monkeypatch):
        """Items with existing GitHub issues are recorded as duplicate-skipped."""
        learnings_path = tmp_path / "LEARNINGS.md"
        learnings_path.write_text(SAMPLE_LEARNINGS)
        monkeypatch.setattr(apl, "get_learnings_path", lambda: learnings_path)

        record = apl.PromotedIssueRecord()

        with (
            patch.object(apl, "detect_target_repo", return_value=("o", "r")),
            patch.object(apl, "PromotedIssueRecord") as mock_cls,
            patch.object(apl, "has_existing_issue", return_value=True),
        ):
            mock_cls.load.return_value = record
            result = apl.run_auto_promote()

        assert result == []


