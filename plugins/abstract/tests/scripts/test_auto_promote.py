    def test_empty_content_returns_empty(self):
        """Empty content returns empty list."""
        assert apl.parse_improvement_items("") == []

    @pytest.mark.unit
    def test_high_impact_issues_extracted(self):
        """Items from High-Impact Issues section are returned."""
        items = apl.parse_improvement_items(SAMPLE_LEARNINGS)
        skills = [i["skill"] for i in items]
        assert "abstract:skill-auditor" in skills

    @pytest.mark.unit
    def test_slow_skill_extracted(self):
        """Items from Slow Execution section are returned."""
        items = apl.parse_improvement_items(SAMPLE_LEARNINGS)
        skills = [i["skill"] for i in items]
        assert "abstract:slow-skill" in skills

    @pytest.mark.unit
    def test_slow_skill_not_duplicated_if_already_in_high_impact(self):
        """Skills already in high-impact aren't duplicated from slow section."""
        # abstract:skill-auditor is in high-impact, not slow; abstract:slow-skill
        # is only in slow, so it should appear exactly once
        items = apl.parse_improvement_items(SAMPLE_LEARNINGS)
        slow_items = [i for i in items if i["skill"] == "abstract:slow-skill"]
        assert len(slow_items) == 1

