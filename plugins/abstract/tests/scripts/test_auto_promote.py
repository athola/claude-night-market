class TestPromotedIssueRecord:
    """PromotedIssueRecord persists promoted items."""

    @pytest.mark.unit
    def test_new_record_is_empty(self, tmp_path):
        """When no record file exists, promoted dict is empty."""
        record = apl.PromotedIssueRecord.load(tmp_path / "promoted.json")
        assert record.promoted == {}

    @pytest.mark.unit
    def test_is_promoted_false_for_unknown_key(self, tmp_path):
        """is_promoted returns False for an unknown key."""
        record = apl.PromotedIssueRecord.load(tmp_path / "promoted.json")
        assert not record.is_promoted("p:s:high_failure_rate")

    @pytest.mark.unit
    def test_add_and_is_promoted(self, tmp_path):
        """After add(), is_promoted returns True."""
        record = apl.PromotedIssueRecord()
        record.add("p:s:high_failure_rate", "https://example.com/1")
        assert record.is_promoted("p:s:high_failure_rate")

    @pytest.mark.unit
    def test_save_and_reload(self, tmp_path):
        """Save followed by load gives the same record."""
        path = tmp_path / "promoted.json"
        record = apl.PromotedIssueRecord()
        record.add("p:s:type", "https://url")
        record.save(path)

        reloaded = apl.PromotedIssueRecord.load(path)
        assert reloaded.is_promoted("p:s:type")
        assert reloaded.promoted["p:s:type"] == "https://url"

    @pytest.mark.unit
    def test_bad_json_returns_empty(self, tmp_path):
        """Malformed JSON file returns empty record."""
        path = tmp_path / "promoted.json"
        path.write_text("INVALID")
        record = apl.PromotedIssueRecord.load(path)
        assert record.promoted == {}


