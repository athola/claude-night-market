class TestTrackIssuePersistence:
    """Feature: Detect issues persisting across N posts"""

    @pytest.mark.unit
    def test_issue_in_all_recent_history_is_persistent(self) -> None:
        """Scenario: Same issue in last two posts plus today
        Given threshold=3 and history showing the same fingerprint twice
        When tracked
        Then the issue is flagged as persistent
        """
        issues = [
            EnrichedIssue(
                skill="a:b",
                issue_type="failure_rate",
                severity="high",
                metric="",
                detail="",
            )
        ]
        history = [
            ["a:b|failure_rate", "other:thing|x"],
            ["a:b|failure_rate"],
        ]
        result = track_issue_persistence(issues, history, threshold=3)
        assert len(result) == 1
        assert result[0]["skill"] == "a:b"
        assert result[0]["streak"] == 3

    @pytest.mark.unit
    def test_issue_missing_from_recent_history_not_flagged(self) -> None:
        """Scenario: Issue is new today
        Given history without this issue's fingerprint
        When tracked with threshold=3
        Then result is empty
        """
        issues = [
            EnrichedIssue(
                skill="a:b", issue_type="t", severity="high", metric="", detail=""
            )
        ]
        history = [["other|other"], ["other|other"]]
        assert track_issue_persistence(issues, history, threshold=3) == []

    @pytest.mark.unit
    def test_threshold_below_2_returns_empty(self) -> None:
        """Scenario: Invalid threshold
        Given threshold=1 (would mean 'persistent if seen once')
        When tracked
        Then empty list (degenerate case)
        """
        issues = [
            EnrichedIssue(
                skill="a", issue_type="t", severity="high", metric="", detail=""
            )
        ]
        assert track_issue_persistence(issues, [["a|t"]], threshold=1) == []


