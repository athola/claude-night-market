class TestClusterFailureModes:
    """Feature: Cluster similar errors into named failure modes

    As a reader
    I want errors with shared significant terms grouped under one name
    So that 24 individual "validation failed" lines become one named mode
    """

    @pytest.mark.unit
    def test_groups_errors_sharing_significant_terms(self) -> None:
        """Scenario: Three errors all mention 'frontmatter'
        Given errors_by_skill with three frontmatter-related errors
        When cluster_failure_modes runs
        Then one FailureMode is returned named after the dominant term
        """
        errors = {
            "abstract:skill-auditor": [
                "validation failed missing frontmatter description",
                "validation failed missing frontmatter name",
            ],
            "abstract:rules-eval": [
                "validation failed missing frontmatter trigger",
            ],
        }
        modes = cluster_failure_modes(errors)
        assert len(modes) == 1
        mode = modes[0]
        assert mode.name in {"frontmatter", "missing", "validation"}
        assert mode.occurrences == 3
        assert "abstract:skill-auditor" in mode.skills
        assert "abstract:rules-eval" in mode.skills

    @pytest.mark.unit
    def test_singleton_errors_do_not_form_a_mode(self) -> None:
        """Scenario: Each skill has unique errors
        Given errors with no shared terms across skills
        When clustered
        Then no FailureMode is produced (below MIN_ERRORS threshold)
        """
        errors = {
            "skill:a": ["network timeout to upstream"],
            "skill:b": ["division by zero in calculator"],
        }
        modes = cluster_failure_modes(errors)
        assert modes == []

    @pytest.mark.unit
    def test_empty_input_returns_empty(self) -> None:
        """Scenario: No errors to cluster
        Given an empty errors_by_skill dict
        When clustered
        Then result is empty list
        """
        assert cluster_failure_modes({}) == []


