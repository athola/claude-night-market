class TestComputeTrendDeltas:
    """Feature: Compare current snapshot to previous

    As a reader
    I want to know what changed since last post
    So that I can spot regressions and improvements at a glance
    """

    @pytest.mark.unit
    def test_no_previous_snapshot_returns_all_as_new(self) -> None:
        """Scenario: First post ever
        Given no previous snapshot
        When deltas computed
        Then all current skills are 'new'
        """
        current = {"a": {"success_rate": 90, "total_executions": 10}}
        deltas = compute_trend_deltas(current, None)
        assert deltas["new_skills"] == ["a"]
        assert deltas["resolved_skills"] == []

    @pytest.mark.unit
    def test_significant_rate_change_recorded(self) -> None:
        """Scenario: Success rate dropped 30 percentage points
        Given previous=70, current=40
        When deltas computed
        Then a rate_change entry appears with delta=-30
        """
        prev = {"a": {"success_rate": 70, "total_executions": 100}}
        cur = {"a": {"success_rate": 40, "total_executions": 100}}
        deltas = compute_trend_deltas(cur, prev)
        assert any(
            c["skill"] == "a" and c["delta"] == -30 for c in deltas["rate_changes"]
        )

    @pytest.mark.unit
    def test_small_rate_change_filtered_out(self) -> None:
        """Scenario: 2pp change is noise
        Given previous=70, current=72
        When deltas computed
        Then no rate_changes entry is produced
        """
        prev = {"a": {"success_rate": 70, "total_executions": 100}}
        cur = {"a": {"success_rate": 72, "total_executions": 100}}
        deltas = compute_trend_deltas(cur, prev)
        assert deltas["rate_changes"] == []

    @pytest.mark.unit
    def test_skill_disappearance_recorded_as_resolved(self) -> None:
        """Scenario: Previous had skill 'b', current does not
        Given previous={a, b}, current={a}
        When deltas computed
        Then 'b' is in resolved_skills
        """
        prev = {
            "a": {"success_rate": 90, "total_executions": 10},
            "b": {"success_rate": 50, "total_executions": 5},
        }
        cur = {"a": {"success_rate": 90, "total_executions": 10}}
        deltas = compute_trend_deltas(cur, prev)
        assert deltas["resolved_skills"] == ["b"]


